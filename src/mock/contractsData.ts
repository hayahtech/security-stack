// Calculate KPIs
export function calculateClientContractsKPIs() {
  const activeRecurring = clientContracts.filter(c => c.type === "recorrente" && (c.status === "ativo" || c.status === "vence_em_breve"));
  const mrr = activeRecurring.reduce((sum, c) => sum + c.monthlyValue, 0);
  const arr = mrr * 12;
  
  // Backlog = sum of all future contracted values
  const backlog = clientContracts.reduce((sum, c) => {
    if (c.type === "projeto") return sum + (c.totalValue || 0);
    const monthsRemaining = Math.max(0, (c.daysToExpire || 0) / 30);
    return sum + (c.monthlyValue * monthsRemaining);
  }, 0);
  
  const expiring90d = clientContracts.filter(c => (c.daysToExpire || 999) <= 90 && c.status !== "encerrado");
  const expiringValue = expiring90d.reduce((sum, c) => sum + c.monthlyValue, 0);
  
  return { mrr, arr, backlog, expiring90dCount: expiring90d.length, expiring90dValue: expiringValue, renewalRate: 87 };
}

export function calculateSupplierContractsKPIs() {
  const active = supplierContracts.filter(c => c.status === "ativo" || c.status === "vence_em_breve" || c.status === "negociacao");
  const monthlyCommitment = active.reduce((sum, c) => sum + c.monthlyValue, 0);
  const annualCommitment = monthlyCommitment * 12;
  
  return { monthlyCommitment, annualCommitment, totalContracts: supplierContracts.length };
}
