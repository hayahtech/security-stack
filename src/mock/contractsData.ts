export type ContractType = "recorrente" | "projeto" | "avulso";
export type ContractStatus = "ativo" | "vence_em_breve" | "em_andamento" | "encerrado" | "negociacao";
export type AdjustmentIndex = "IPCA" | "IGP-M" | "INCC" | "ANS" | "fixo" | "nenhum";

export interface ContractPayment {
  id: string;
  date: string;
  value: number;
  status: "pago" | "pendente" | "atrasado";
}

export interface Contract {
  id: string;
  company: string;
  cnpj: string;
  type: ContractType;
  category: "cliente" | "fornecedor";
  object: string;
  monthlyValue: number;
  totalValue?: number;
  startDate: string;
  endDate: string;
  adjustmentIndex: AdjustmentIndex;
  adjustmentDate?: string;
  adjustmentPercent?: number;
  status: ContractStatus;
  nextAction?: string;
  daysToExpire?: number;
  paymentMethod: string;
  cancellationFee: number;
  sla?: string;
  payments: ContractPayment[];
  documents: string[];
  alerts: { days: number; enabled: boolean }[];
  currentStep?: number;
  totalSteps?: number;
}

